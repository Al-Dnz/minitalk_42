#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		size;
	int		i;
	char	*new;

	size = 0;
	while (s1[size])
		size++;
	new = malloc(sizeof(char) * (size + 1));
	if (new == NULL)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		new[i] = s1[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
